import React from 'react';
import { Formik } from 'formik';

const Basic = () => (
  <div>
    <h1>Anywhere in your app!</h1>
    <Formik //3 TANE PROP'U VARDIR : 1)INITIAL.VALUES, 2)VALIDATE, 3)ONSUBMIT

      initialValues={{ email: '', password: '' }}
      //EN TEMEL ELEMENT BUDUR. FORMDAKI INPUTLARIN BASLANGIC DEGERLERI VE ISIMLERI BURADA BELIRTILIR.

      validate={values => {
        //FORM INPUTUNA GIRILEN DEGERLERI, DEGERLENDIRMEYE ALIYORUZ (şu karakterleri içersin vs). HARICI BIR VALIDATION DA KULLANABILIRIZ.
        const errors = {};
        if (!values.email) {
          errors.email = 'Required';
        } else if (
          !/^[A-Z0-9._%+-]+@[A-Z0-9.-]+\.[A-Z]{2,}$/i.test(values.email)
        ) {
          errors.email = 'Invalid email address';
        }
        return errors;
      }}
      onSubmit={(values, { setSubmitting }) => {
        //SUBMIT EDILDIGINDE NELER YAPILACAGINI BU KISMA YAZIYORUZ.
        setTimeout(() => {
          alert(JSON.stringify(values, null, 2));
          setSubmitting(false);
        }, 400);
      }}
    >
      {({
        values,
        errors,
        touched,
        handleChange,
        handleBlur,
        handleSubmit,
        isSubmitting,
        /* and other goodies */
      }) => (
        <form onSubmit={handleSubmit}>
          <input
            type="email"
            name="email"
            onChange={handleChange}
            onBlur={handleBlur}
            value={values.email}
          />
          {errors.email && touched.email && errors.email}
          <input
            type="password"
            name="password"
            onChange={handleChange}
            onBlur={handleBlur}
            value={values.password}
          />
          {errors.password && touched.password && errors.password}
          <button type="submit" disabled={isSubmitting}>
            Submit
          </button>
        </form>
      )}
    </Formik>
  </div>
);

export default Basic;